// Function to update mapData with UNESCO data
export async function updateMapDataFromUnesco() {
    try {
        const transformedData = await getTransformedCountryData();
        if (transformedData && transformedData.length > 0) {
            mapData = transformedData;
        }
    } catch (error) {
        console.error('Failed to update data from UNESCO:', error);
        // Fallback to initial data
        mapData = initialMapData;
    }
}